package com.sun.enterprise.server.logging;

public enum Facility {

    KERN(0),
    USER(8),
    MAIL(16),
    DAEMON(24),
    AUTH(32),
    SYSLOG(40),
    LPR(48),
    NEWS(56),
    UUCP(64),
    CRON(72),
    AUTHPRIV(80),
    FTP(88),
    LOCAL0(128),
    LOCAL1(136),
    LOCAL2(144),
    LOCAL3(152),
    LOCAL4(160),
    LOCAL5(168),
    LOCAL6(176),
    LOCAL7(184);

    private final int value;

    Facility(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public static int fromString(String input) {
        return Facility.valueOf(input.toUpperCase()).getValue();
    }
}
