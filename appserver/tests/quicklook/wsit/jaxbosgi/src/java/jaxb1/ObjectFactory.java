package jaxb1;


/**
 * This object contains factory methods for each 
 * Java content interface and Java element interface 
 * generated in the jaxb1 package. 
 * <p>An ObjectFactory allows you to programatically 
 * construct new instances of the Java representation 
 * for XML content. The Java representation of XML 
 * content can consist of schema derived interfaces 
 * and classes representing the binding of schema 
 * type definitions, element declarations and model 
 * groups.  Factory methods for each of these are 
 * provided in this class.
 * 
 */
public class ObjectFactory
    extends jaxb1.impl.runtime.DefaultJAXBContextImpl
{

    private static java.util.HashMap defaultImplementations = new java.util.HashMap(16, 0.75F);
    private static java.util.HashMap rootTagMap = new java.util.HashMap();
    public static final jaxb1.impl.runtime.GrammarInfo grammarInfo = new jaxb1.impl.runtime.GrammarInfoImpl(rootTagMap, defaultImplementations, (jaxb1.ObjectFactory.class));
    public static final java.lang.Class version = (jaxb1.impl.JAXBVersion.class);

    static {
        defaultImplementations.put((jaxb1.Catalog.class), "jaxb1.impl.CatalogImpl");
        defaultImplementations.put((jaxb1.CatalogType.class), "jaxb1.impl.CatalogTypeImpl");
        rootTagMap.put(new javax.xml.namespace.QName("", "catalog"), (jaxb1.Catalog.class));
    }

    /**
     * Create a new ObjectFactory that can be used to create new instances of schema derived classes for package: jaxb1
     * 
     */
    public ObjectFactory() {
        super(grammarInfo);
    }

    /**
     * Create an instance of the specified Java content interface.
     * 
     * @param javaContentInterface
     *     the Class object of the javacontent interface to instantiate
     * @return
     *     a new instance
     * @throws JAXBException
     *     if an error occurs
     */
    public java.lang.Object newInstance(java.lang.Class javaContentInterface)
        throws javax.xml.bind.JAXBException
    {
        return super.newInstance(javaContentInterface);
    }

    /**
     * Get the specified property. This method can only be
     * used to get provider specific properties.
     * Attempting to get an undefined property will result
     * in a PropertyException being thrown.
     * 
     * @param name
     *     the name of the property to retrieve
     * @return
     *     the value of the requested property
     * @throws PropertyException
     *     when there is an error retrieving the given property or value
     */
    public java.lang.Object getProperty(java.lang.String name)
        throws javax.xml.bind.PropertyException
    {
        return super.getProperty(name);
    }

    /**
     * Set the specified property. This method can only be
     * used to set provider specific properties.
     * Attempting to set an undefined property will result
     * in a PropertyException being thrown.
     * 
     * @param name
     *     the name of the property to retrieve
     * @param value
     *     the value of the property to be set
     * @throws PropertyException
     *     when there is an error processing the given property or value
     */
    public void setProperty(java.lang.String name, java.lang.Object value)
        throws javax.xml.bind.PropertyException
    {
        super.setProperty(name, value);
    }

    /**
     * Create an instance of Catalog
     * 
     * @throws JAXBException
     *     if an error occurs
     */
    public jaxb1.Catalog createCatalog()
        throws javax.xml.bind.JAXBException
    {
        return new jaxb1.impl.CatalogImpl();
    }

    /**
     * Create an instance of CatalogType
     * 
     * @throws JAXBException
     *     if an error occurs
     */
    public jaxb1.CatalogType createCatalogType()
        throws javax.xml.bind.JAXBException
    {
        return new jaxb1.impl.CatalogTypeImpl();
    }

}
