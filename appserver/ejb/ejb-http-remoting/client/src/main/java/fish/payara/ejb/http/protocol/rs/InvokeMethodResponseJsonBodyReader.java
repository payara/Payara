package fish.payara.ejb.http.protocol.rs;

import fish.payara.ejb.http.protocol.InvokeMethodResponse;
import fish.payara.ejb.http.protocol.MediaTypes;

import javax.json.bind.Jsonb;
import javax.json.bind.JsonbBuilder;
import javax.json.bind.JsonbConfig;
import javax.ws.rs.Consumes;
import javax.ws.rs.WebApplicationException;
import javax.ws.rs.core.MediaType;
import javax.ws.rs.core.MultivaluedMap;
import javax.ws.rs.ext.MessageBodyReader;
import javax.ws.rs.ext.Provider;
import java.io.IOException;
import java.io.InputStream;
import java.lang.annotation.Annotation;
import java.lang.reflect.Type;

@Provider
@Consumes(MediaTypes.JSON)
public class InvokeMethodResponseJsonBodyReader implements MessageBodyReader<InvokeMethodResponse> {
    @Override
    public boolean isReadable(Class<?> type, Type genericType, Annotation[] annotations, MediaType mediaType) {
        return mediaType.toString().equals(MediaTypes.JSON)
                && InvokeMethodResponse.ResultType.isPresent(annotations);
    }

    @Override
    public InvokeMethodResponse readFrom(Class<InvokeMethodResponse> type, Type genericType, Annotation[] annotations, MediaType mediaType, MultivaluedMap<String, String> httpHeaders, InputStream entityStream) throws IOException, WebApplicationException {
        Type returnType = InvokeMethodResponse.ResultType.find(annotations).type;
        Jsonb jsonb = JsonbBuilder.newBuilder()
                .withConfig(new JsonbConfig().withDeserializers(new InvokeMethodResponseDeserializer(returnType)))
                .build();
        return jsonb.fromJson(entityStream, InvokeMethodResponse.class);
    }
}
